package week1.day1;

public class LearnDatatypes {
	
	/*
	 * This program is to learn and implement the various data types and utilize.*/

	public static void main(String[] args) {
		
		//declare a float data type and value		
		float versionType=91.0f;
		
		//declare a String data type and value
		String DevloperType="Google";
		
		//declare a boolean data type and value
		boolean isBeta=false;
		
		//declare a int data type and value
		int releaseYear=2008;
		
		//declare a char data type and value
		char shortcutKey='C';
		
		//Print all the declared values of the various data types
		System.out.println("The type of version is "+versionType);
		
		System.out.println("The Developer type is "+DevloperType);
		
		System.out.println("The Beta type is "+isBeta);
		
		System.out.println("The release year is "+releaseYear);
		
		System.out.println("The shortcut key is "+shortcutKey);

	}

}
